#include <string.h>

void	ft_bzero(void *s, size_t n)
{
	char *ptr_cp_s;

	ptr_cp_s = (char *)s;
	while (n--)
	{
		*ptr_cp_s = 0;
		ptr_cp_s++;
	}
}
